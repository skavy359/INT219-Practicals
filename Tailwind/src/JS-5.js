// Arithmetic Operators
console.log("Arithmetic Operators:");

let a = 10, b = 5;

console.log("a + b is", a + b);   // Addition
console.log("a - b is", a - b);   // Subtraction
console.log("a * b is", a * b);   // Multiplication
console.log("a / b is", a / b);   // Division
console.log("a % b is", a % b);   // Modulus (Remainder)
console.log("a ** b is", a ** b); // Exponentiation (ES6 feature)

// Increment and Decrement
console.log("++a is", ++a);       // Pre-increment
console.log("a++ is", a++);       // Post-increment
console.log("Value of a after a++ is", a);

console.log("--b is", --b);       // Pre-decrement
console.log("b-- is", b--);       // Post-decrement
console.log("Value of b after b-- is", b);